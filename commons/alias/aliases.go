package alias

// All represents any possible value
type All = any

// Bit represents a boolean value, either 'true' or 'false'
type Bit = bool

// Int represents a 64-bit whole number aka. 'long'
type Int = int64

// Dec represents a 64-bit decimal number aka. 'double'
type Dec = float64

// Let represents a single character, aka. 'char'
type Let = rune

// Txt represents a string
type Txt = string
